# 按通识课、专业课分组排列，同代码即同记录
# ══════════════════════════════════════════════════════════

def _course(name, code, course_type, college=None):
    """创建或获取课程（按 code 去重）。"""
    defaults = {"name": name, "course_type": course_type}
    if college:
        defaults["college"] = college
    obj, created = Course.objects.get_or_create(code=code, defaults=defaults)
    if not created and (obj.name != name or obj.course_type != course_type):
        obj.name = name
        obj.course_type = course_type
        if college:
            obj.college = college
        obj.save()
    return obj


